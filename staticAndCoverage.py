
def add(a, b):
    """
    Function for adding two inputs
    """
    return a + b


def subtract(a, b):
    """
    Function for subtracting two inputs
    """
    return a - b


def multiply(a, b):
    """
    Function for multiplying two inputs
    """
    return a * b


def divide(numerator, denominator):
    """
    Function for dividing two inputs
    """
    return float(numerator) / denominator
